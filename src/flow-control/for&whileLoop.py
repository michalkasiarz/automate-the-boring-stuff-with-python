# For loop

print("My name is")
for i in range(5):
    print("Jimmy Five Times " + str(i))


# while loop

print("My name is")
i = 0
while i < 5:
    print("Jimmy Five Times " + str(i))
    i += 1

# for loop with range and step backwards

print("My name is")
for i in range(5, -1, -1):
    print("Jimmy Five Times " + str(i))
